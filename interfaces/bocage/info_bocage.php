          <p>
          <br>
          Le «&nbsp;<a href="https://fr.wikipedia.org/wiki/Bocage" target="_blank" data-toggle="tooltip" data-placement="top" title="Définition Wikipedia">Bocage</a>&nbsp;» est un élément constitutif et identitaire du Parc naturel régional Normandie-Maine qui œuvre depuis sa création à sa préservation et sa restauration.<br>
          </p>
          <p>
          Cette carte présente l’évolution de la densité bocagère qui se définit par le ratio entre le linéaire de haies et la surface considérée. Ce ratio constitue un bon indicateur de l’état du bocage.
          </p>
          <p>
          Partout en France, il a connu un fort recul depuis la moitié du 20ème siècle, notamment en raison des remembrements et de la modernisation agricole.<br>
          </p>
          <p>
          Pourtant, les haies rendent de nombreux services (production de bois et de fruits, lutte contre l’érosion, abri pour la faune, effet brise vent, …) et les paysages de bocage constituent un atout en terme d’attractivité résidentielle et touristique.
          </p>
