              <h3>La méthode Parc</h3>
              <p>
              Les haies ont été cartographiées par photo-interprétation [1] sur les clichés aériens de l’IGN (Bd-Ortho) disponibles permettant de constituer une base de données de l’évolution du linéaire de haies depuis les années 50 (approche diachronique).<br>
              Cette base de données permet d’analyser la structure du réseau de haies au travers de différents indicateurs (densité, cohérence, ...), et ce à une échelle très fine.<br><br>
              <small>[1] La photo-interprétation est un processus manuel de reconnaissance d’objets cartographiques (ici les haies), par opposition à la télédétection qui consiste en un processus automatisé. La bibliographie sur le sujet permettait de conclure (au moins en 2011) que la reconnaissance automatisée des haies sur photos aériennes souffrait de biais trop importants. La marge d’erreur des données a été estimée à moins de 5% (surévaluation 1%, sous évaluation 5%).</small>
              </p>
              
